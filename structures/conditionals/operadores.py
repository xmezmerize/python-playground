# Operadores

"""
Dentro de estruturas em Python, podemos usar diversos tipos
de operadores para relacionar situações e condições

# ================================================================================================================== #

1. Operadores relacionais (comparação) -> são usados para
comparar valores e retornam sempre True ou False.

== → igual a
!= → diferente de
> → maior que
< → menor que
>= → maior ou igual a
<= → menor ou igual a

# ================================================================================================================== #

2. Operadores lógicos -> permitem combinar condições:

and → todas as condições precisam ser verdadeiras
or → pelo menos uma condição precisa ser verdadeira
not → inverte o valor lógico

# ================================================================================================================== #

3. Operadores de pertencimento -> usados para verificar se algo
está dentro de uma sequência (list, str, tuple, etc.).

in → está contido
not in → não está contido

# ================================================================================================================== #

4. Operadores de identidade -> verificam se duas variáveis
apontam para o mesmo objeto na memória.

is
is not

# ================================================================================================================== #

5. Operadores bitwise (bit a bit) -> eles trabalham diretamente
nos bits dos números inteiros:

& → AND bit a bit
| → OR bit a bit
^ → XOR bit a bit (ou exclusivo)
~ → NOT bit a bit (inversão)
<< → shift para a esquerda (desloca os bits, multiplica por potências de 2)
>> → shift para a direita (divide por potências de 2, descartando resto)
"""